/* ===============================
   ELEMENTS
================================ */
const categoryList = document.getElementById("categoryList");
const productsGrid = document.getElementById("productsGrid");

/* ===============================
   RENDER CATEGORIES
================================ */
function renderCategories() {
    categoryList.innerHTML = "";

    /* ALL PRODUCTS BUTTON */
    const allLi = document.createElement("li");
    allLi.textContent = "All Products";
    allLi.classList.add("active");
    allLi.onclick = () => {
        setActive(allLi);
        renderProducts(productData);
    };
    categoryList.appendChild(allLi);

    /* UNIQUE CATEGORY LIST */
    const categories = [...new Set(productData.map(p => p.category))];

    categories.forEach(category => {
        const li = document.createElement("li");
        li.textContent = category;
        li.onclick = () => {
            setActive(li);
            filterProducts(category);
        };
        categoryList.appendChild(li);
    });
}

function showProductDetails(slug) {
    const product = productData.find(p => p.slug === slug);
    if (!product) return;

    productsGrid.classList.add("details-mode");

    productsGrid.innerHTML = `
        <div class="product-details">

            <button class="back-btn" onclick="backToProducts()">
                ← Back to Products
            </button>

            <h2 class="product-title">${product.category}</h2>

            <div class="product-top">
                
                <img src="${product.images[0]}" alt="">
            </div>

          <div class="details-section">
    <h3>⚙ Standard Style</h3>
    <ul>
        ${product.details.standardStyle.map(item => {
            const [key, value] = item.split(":");
            return `
                <li>
                    <strong>${key}:</strong>
                    <span>${value}</span>
                </li>
            `;
        }).join("")}
    </ul>
</div>


            <div class="details-split">
                <div>
                    <h3>⚙ Seal Characteristics</h3>
                    <ul>
                        ${product.details.characteristics.map(i => `<li>${i}</li>`).join("")}
                    </ul>
                </div>

                <div>
                    <h3>⚙ Operating Limits</h3>
                    <ul>
                        ${product.details.operatingLimits.map(i => `<li>${i}</li>`).join("")}
                    </ul>
                </div>
            </div>

            <div class="details-bottom">
                <img src="${product.images[1]}" alt="">

                <div>
                    <h3>⚙ Applications</h3>
                    <ul>
                        ${product.details.applications.map(i => `<li>${i}</li>`).join("")}
                    </ul>
                </div>
            </div>

        </div>
    `;
}


function backToProducts() {
    productsGrid.classList.remove("details-mode");
    renderProducts(productData);
}



/* ===============================
   RENDER PRODUCTS
================================ */
function renderProducts(products) {
    productsGrid.innerHTML = "";

    products.forEach(p => {
        const thumbnail = p.images?.[0] || "";

        productsGrid.innerHTML += `
            <div class="product-card">
                <img src="${thumbnail}" alt="${p.category}">
                <h4>${p.category}</h4>
                <div class="product-actions">
                    <button class="read-more" onclick="showProductDetails('${p.slug}')">
                        READ MORE →
                    </button>
                    <a href="contact.html" class="enquiry-btn-small">
                        ENQUIRY →
                    </a>
                </div>
            </div>
        `;
    });
}



/* ===============================
   FILTER PRODUCTS
================================ */
function filterProducts(category) {
    const filtered = productData.filter(p => p.category === category);
    renderProducts(filtered);
}

/* ===============================
   ACTIVE STATE HANDLER
================================ */
function setActive(activeEl) {
    document
        .querySelectorAll(".category-list li")
        .forEach(li => li.classList.remove("active"));

    activeEl.classList.add("active");
}

/* ===============================
   INIT
================================ */
renderCategories();
renderProducts(productData);
